// switch case


const month = "dec"

switch (month) {
    case 1, "jan":
        console.log("January");
        break;
    case 2, "feb":
        console.log("February");
        break;
    case 3, "march":
        console.log("March");
        break;
    case 4, "april":
        console.log("April");
        break;
    case 5, "may":
        console.log("May");
        break;
    case 6, "june":
        console.log("June");
        break;
    case 7, "july":
        console.log("July");
        break;
    case 8, "aug":
        console.log("August");
        break;
    case 9, "sep":
        console.log("September");
        break;
    case 10, "oct":
        console.log("October");
        break;
    case 11, "nov":
        console.log("November");
        break;
    case 12, "dec":
        console.log("December");
        break;

    default:
        console.log("Default case / wrong input");
        break;
}