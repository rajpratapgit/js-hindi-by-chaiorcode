// while loop

let index = 0;
while (index <= 10) {
  console.log(`Value of index is: ${index}`);
  index += 2;
}

let myArr = ["Flash", "BatMan", "IronMan"];
let i = 0;
while (i < myArr.length) {
  console.log(`Heroes name is: ${myArr[i]}`);
  i++;
}

// do while loop

let score = 11;
do {
  console.log(`Score is: ${score}`);
  score++;
} while (score <= 10);
